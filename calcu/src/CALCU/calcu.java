package CALCU;

public class calcu {

    public static int suma (int num1, int num2){
        return num1 + num2;
    }

    public static int resta (int num1, int num2){
        return num1 - num2;
    }

    public static int multiplicar (int num1, int num2){
        return num1 * num2;
    }

    public static double dividir (int num1, int num2){
        return num1 / num2;
    }

}

